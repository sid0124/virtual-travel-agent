export function buildDestinationImageUrl(input: {
  name: string
  state?: string
  city?: string
  country?: string
  category?: string
}) {
  return buildDestinationPlaceholderImage(input)
}

export function ensureDestinationImage<T extends Destination>(destination: T): T {
  const image = destination.image?.trim()

  return {
    ...destination,
    image: image && image !== ""
      ? image
      : buildDestinationImageUrl({
          name: destination.name,
          state: destination.state,
          city: destination.city,
          country: destination.country,
          category: destination.category || destination.type,
        }),
  }
}

